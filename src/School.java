public class School {
    private String name;
    private String adres;
    private int number;

    public void setName(String name) {
        this.name = name;
    }

    public void setAdres(String adres) {
        this.adres = adres;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getAdres() {
        return adres;
    }

    public int getNumber() {
        return number;
    }
}
